    private void jButtonSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarClienteActionPerformed
        String nome_cliente = jTextNomeCliente.getText();
        String endereco_cliente = jTextEnderecoCliente.getText();
        String telefone_cliente = jTextTelefoneCliente.getText();
        
        if (nome_cliente.equals("") || endereco_cliente.equals("") || telefone_cliente.equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
        } else {
            try {
                ConexaoBD con = new ConexaoBD();
                java.sql.Statement st = (java.sql.Statement) con.conexao.createStatement();
                
                String sql = "INSERT INTO cliente (nome_cliente, endereco_cliente, telefone_cliente) VALUES('" + nome_cliente + "','" + endereco_cliente + "','" + telefone_cliente + "')";
                
                if (st.execute(sql)) {
                    JOptionPane.showMessageDialog(null, "Ops! Algo errado");
                } else {
                    JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
                    LimparCampos();
        
            
                }
            }   catch (SQLException ex) {
                    Logger.getLogger(CadastrarCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButtonSalvarClienteActionPerformed
    }
